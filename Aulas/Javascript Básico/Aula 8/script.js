/* Operadores de comparação

== Igualdade de valor

=== Igualdade de valor e tipo (usa-se com mais frequência)

!= difenreça de valores

!== diferença de valores e tipo

> maior que

< menor que

>= maior ou igual

<= menor ou igual

*/


let a = 10
let b = '10'

console.log(a===b) // false
console.log(a==b) // true
console.log(a!=b) // false
console.log(a!==b) // true
console.log(a<b) // false
console.log(a>b) // false
console.log(a>=b) // true
console.log(a<=b) // true